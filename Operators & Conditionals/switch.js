// switch statement
// it works as an simplified if statements 
// switch -> varible -> {Condition} -> case and default (work as if and else)

let example = Math.floor(Math.random() *11);
switch(example){
    case 9 :
        console.log('true');
        break;

    case 10 :
          console.log('true');
        break;

    default:
          console.log('not 9 or 10');
        break;
};

console.log(Math.floor(Math.random() * 11));












